                    <p class="proximanova-bold primary-font-color px-12-font text-justify"><span>Steps in the assessment
                            process</span></p>
                    <p class="proximanova-bold px-12-font text-justify"><span>Step 1: The assessment interview</span>
                    </p>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">The assessment process should
                        begin with an interview with the candidate. Use the PART A of test to guide the interview.</p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">The assessor should put the
                        candidate at ease and explain that the purpose of the LLN test interview is to gather
                        information about their educational background and LLN skill level to help determine future
                        training support needs. They should also explain that the assessment will include a one-to-one
                        chat and then some time will be given for the candidate to complete tasks of PART B. Assessors
                        should encourage candidates to feel comfortable and ask questions at any time.</p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">The interview is an opportunity
                        for the assessor to gather information about the candidate’s oral communication and learning
                        skills. Assessors can also start thinking about which tasks may be appropriate, depending on the
                        candidate’s background, interest areas and future training goals.</p>
                    <div class="clearfix" style="height: 10px;"></div>

                    <p class="px-11-font text-justify line-height-1point2 text-justify">PART A should be completed by
                        the assessor in conversation with the candidate. Not all questions will be relevant. The form
                        includes prompts for the assessor to flesh out questions where appropriate, but they could also
                        use their own prompts.</p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">The self-reflection (included in
                        PART A) is also designed to be completed through interview. It is a task to encourage candidates
                        to talk about the skills they have and the skills they would like to develop.</p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Because they are conducted
                        orally, the assessment interview and the self-assessment are ways of specifically collecting
                        information about the candidate’s oral communication skills. </p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Ideally, an assessment interview
                        would be conducted in a quiet place on a one-to-one basis.</p>
                    <div class="clearfix" style="height: 10px;"></div>

                    <p class="proximanova-bold px-12-font text-justify"><span>Step 2: PART B Assessment tasks</span></p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">There are two assessment tasks
                        in PART B. These are useful to get an idea of the candidate’s skill level. The tasks can provide
                        valuable information about the candidate’s ability to cope with specific skills and language
                        relevant to particular training.</p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Remember to consider the
                        support, context, text and task complexity factors when selecting the tasks. For example, a
                        level 3 task given with a high level of support will deem the task level 2 in many cases.</p>


