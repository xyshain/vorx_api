                    <p
                        class="section-header proximanova-bold primary-font-color px-14-font text-justify text-uppercase">
                        <span>Introduction</span></p>
                    <p class="px-11-font text-justify line-height-1point2">This test includes a range of tasks designed
                        to be used to identify an individual’s level in the core language, literacy and numeracy (LLN)
                        skills of Learning, Reading, Writing, Oral Communication and Numeracy. The tasks in this test
                        are adapted from tasks bank developed by Precision Group in collaboration with Commonwealth of
                        Australia. </p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2">The assessment tasks align with the
                        Australian Core Skills Framework (ACSF). The ACSF is a national framework that provides:</p>
                    <ul class="px-10-font text-justify no-margin paragraph-list">
                        <li class="line-height-1point2">a consistent national approach to the identification of the core
                            LLN skills requirements in diverse work, training, personal and community contexts</li>
                        <li class="line-height-1point2">a common reference point for describing and discussing
                            performance in the five core LLN skill areas.</li>
                    </ul>

                    <p class="proximanova-bold primary-font-color px-12-font text-justify"><span>Why assess core LLN
                            skill levels?</span></p>
                    <p class="px-11-font text-justify line-height-1point2">Assessment of core LLN skill levels
                        identifies an individual’s skill levels. This individual assessment can then be compared with
                        the LLN levels required of a training program (or unit or qualification), or workplace tasks.
                        The person may be a new entrant to the training sector, or may be following a training pathway
                        to a new course or qualification. </p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2">The purpose of the assessment should be to
                        determine whether the person:</p>
                    <ul class="px-10-font text-justify no-margin paragraph-list">
                        <li class="line-height-1point2">should complete an LLN bridging program before commencing the
                            training, or</li>
                        <li class="line-height-1point2">requires LLN support throughout the training program (or unit or
                            qualification) or workplace tasks.</li>
                    </ul>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Identified gaps can be addressed
                        in various ways, for example bridging courses, specialist LLN practitioner support, or
                        activities provided by a vocational trainer with knowledge of developing LLN skills. </p>

