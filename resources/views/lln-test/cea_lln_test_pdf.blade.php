                    <p class="proximanova-bold primary-font-color px-12-font text-justify"><span>Mapping of levels and
                            indicators</span></p>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Each of the tasks included is
                        mapped as given above. Each task is mapped to one or more skill level indicators, and the
                        aspects of communication have also been identified.</p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">ACSF levels refer to exit
                        levels; that is, the person is able to demonstrate what is required at that level. Users should
                        always remember the factors that may influence a person’s performance at each level (in each
                        core skill). These are:</p>
                    <ul class="px-10-font text-justify no-margin paragraph-list">
                        <li class="line-height-1point2">the degree and nature of <span
                                class="proximanova-bold">support</span> available</li>
                        <li class="line-height-1point2">he familiarity with the <span
                                class="proximanova-bold">context</span></li>
                        <li class="line-height-1point2">the complexity of <span class="proximanova-bold">text</span>
                        </li>
                        <li class="line-height-1point2">the complexity of the <span
                                class="proximanova-bold">task</span>.</li>
                    </ul>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">The level of skill demonstrated
                        by a candidate can be changed by the factors, for example, if significant support is given to a
                        candidate completing a level 3 task, the outcome of the assessment could be that the candidate
                        is at exit level 2. </p>
                    <div class="clearfix" style="height: 10px;"></div>
