                    <p class="proximanova-bold px-12-font text-justify"><span>Step 3: Test by candidate</span></p>

                    <p class="px-11-font text-justify line-height-1point2 text-justify">Ask the candidate to complete
                        the first task and offer support if required. Remember that the level of support provided can
                        change the level of skill noted in the task mapping. Ask the candidate to complete the tasks.
                    </p>
                    <div class="clearfix" style="height: 10px;"></div>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Thank the candidate for
                        completing the test and inform them about the process that will follow, which should include
                        letting them know about any recommendations. </p>
                    <div class="clearfix" style="height: 10px;"></div>


                    <p class="proximanova-bold px-12-font text-justify"><span>Step 4: Make the assessment
                            judgement</span></p>
                    <p class="px-11-font text-justify line-height-1point2 text-justify">Based on your knowledge of the
                        ACSF skill levels, make a judgement about the candidate’s skill level. When making the
                        judgement, remember to consider the support provided, the context, the task and the task
                        complexity. For example, the candidate may have completed an ACSF level 3 task, but was only
                        able to do it with support. The assessor will need to make a decision about whether this
                        candidate is perhaps only performing at ACSF level 2 or lower. If the candidate’s performance
                        indicates that they are not operating at the required ACSF level to complete the training
                        successfully it might be necessary to recommend LLN support prior to, or during the training.
                    </p>
                    <div class="clearfix" style="height: 10px;"></div>


                    <p class="proximanova-bold px-12-font text-justify"><span>Step 5: Communicate the assessment
                            decision to the candidate</span></p>

                    <p class="px-11-font text-justify line-height-1point2 text-justify">It is good practice to ensure
                        that candidates understand more about their LLN skill levels, having undertaken the assessment.
                        Explain the results of the assessment to the candidate, what the levels mean, and how they may
                        impact on and training that the candidate may undertake. Answer any questions that candidates
                        may have.</p>
                    <div class="clearfix" style="height: 10px;"></div>

                    <p class="proximanova-bold px-12-font text-justify"><span>Step 6: Complete the report</span></p>

                    <p class="px-11-font text-justify line-height-1point2 text-justify">Fill out the ACSF assessment
                        record with details of the assessment.</p>

